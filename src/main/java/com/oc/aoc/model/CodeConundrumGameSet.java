package com.oc.aoc.model;

public class CodeConundrumGameSet {

    private int blue;
    private int red;
    private int green;

    public CodeConundrumGameSet(int blue, int red, int green) {
        this.blue = blue;
        this.red = red;
        this.green = green;
    }

    public Integer getRed() {
        return red;
    }

    public Integer getBlue() {
        return blue;
    }

    public Integer getGreen() {
        return green;
    }

}